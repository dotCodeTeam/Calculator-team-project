package com.ohgiraffers.run;

public class Calculator {

    static int add(int a1, int b1) {
        return a1 + b1;
    }

    static int divide(int a1,int b1) {
        return a1/b1;
    }

    public static int mul (int n1, int n2) {
        return n1 * n2;

    }
    public int minus(int a1, int a2){
        return a1 - a2;
    }
}
